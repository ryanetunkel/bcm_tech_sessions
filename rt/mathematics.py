"""Some basic math functions"""
import math


def add_nums(a: int, b: int) -> int:
    return a + b


def subtract_nums(a: int, b: int) -> int:
    return a - b


def multiply_nums(a: int, b: int) -> int:
    return a * b


def divide_nums(a: int, b: int) -> int:
    return a / b


def square_num(a: int) -> int:
    return a * a


def sqrt_num(a: int) -> int:
    return math.sqrt(a)


def cube_num(a: int) -> int:
    return a * a * a


def cbrt_num(a: int) -> int:
    return math.cbrt(a)
